package_installed() {
    package=$1
    yay -Qi $package &> /dev/null
}

install_packages(){
    packages=("$@")
    install_list=()
    for package in "${packages[@]}"; do
        if ! package_installed $package; then
            install_list+=("$package")
        fi
    done

    if ! [ ${#install_list[@]} -eq 0 ]; then
        for pkg in "${install_list[@]}"; do
            echo "installing $pkg..."
            yay -S --noconfirm $pkg
        done
    fi
}
